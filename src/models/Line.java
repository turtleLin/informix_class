package models;

public class Line {
   private String direct;
   private String lineno;
   private String startstopname;
   private String endstopname;
   private String stopnumafter;
   private String stopname;
public String getStopname() {
	return stopname;
}
public void setStopname(String stopname) {
	this.stopname = stopname;
}
public String getDirect() {
	return direct;
}
public void setDirect(String direct) {
	this.direct = direct;
}
public String getLineno() {
	return lineno;
}
public void setLineno(String lineno) {
	this.lineno = lineno;
}
public String getStartstopname() {
	return startstopname;
}
public void setStartstopname(String startstopname) {
	this.startstopname = startstopname;
}
public String getEndstopname() {
	return endstopname;
}
public void setEndstopname(String endstopname) {
	this.endstopname = endstopname;
}
public String getStopnumafter() {
	return stopnumafter;
}
public void setStopnumafter(String stopnumafter) {
	this.stopnumafter = stopnumafter;
}
   
   
}
